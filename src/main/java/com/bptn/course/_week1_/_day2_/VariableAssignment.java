package com.bptn.course._week1_._day2_;

public class VariableAssignment {
	public static void main(String[] args) {
		int x = 3;
		int y = 2;
		System.out.println(x);
		System.out.println(y);
		x = y;
		System.out.println(x);
		System.out.println(y);
		y = 5;
		System.out.println(x);
		System.out.println(y);
	}
}