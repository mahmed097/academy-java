package com.bptn.course._week1_._day1_;

public class Main1 {
	public static void main(String[] args) {
		// You can declare and initialize a string like any other variable:
		String helloStatement = "Hello, World!";
		// And then use it in your code when you need to:
		System.out.println(helloStatement);
		// But remember to keep the variable name as a name without quotes as shown
		// above, otherwise you won't get the behaviour you're expecting:

		System.out.println("helloStatement");
	}

}
